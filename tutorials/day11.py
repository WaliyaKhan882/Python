#Operators in Python
#Arithmetic Operators
a=5
b=6
print(a+b)
print(6-5)
print(5*6)
print(12/5)
print(12//5) #will give answer only in whole numbers use floor function of math.h(as used in c)
print(5**3) # power function
print(12%5) # modulus will give the remainder
#assignment operator
x=5
print(x)
x+=6 #5+6
print(x)
#comparison Operator
x= 4
print(x==5)
print(x==49%5)
#logical operators
a=True
b=False
print(a and b)
print(a or b)
#Identity Operators
print(5 is 6)
print(5 is not 6)
#Membership Operators
list1=[23,3,45,7,98]
print(23 in list1)
print(45 not in list1)
#Bitwise Operators
a=10 #2
b=11 #3
print(a & b)
print(a|b)
print(0|3) #00 OR 11 == 11 which is 3
print(0&2) #00 AND 10 ==00 which is 0

